	 <center><h3>Sistem Pakar</h3></center>
	    <p>
	    Sistem Pakar (Expert System) adalah sistem yang berusaha mengadopsi pengetahuan manusia ke komputer, agar komputer dapat menyelesaikan masalah seperti yang biasa dilakukan oleh para ahli, dan sistem pakar yang baik dirancang agar dapat menyelesaikan suatu permasalahan tertentu dengan meniru kerja dari para ahli.
	    </p>
	    <p>
	    Tujuan Sistem pakar sendiri merupakan paket perangkat lunak atau paket program komputer yang ditujukan sebagai penyedia nasihat dan sarana bantu dalam memecahkan masalah di bidang-bidang spesialisasi tertentu seperti sains, perekayasaan, matematika, kedokteran, pendidikan dan sebagainya. Sistem pakar merupakan merupakan subset dari Artificial Intelegence.
	    </p>

	    
